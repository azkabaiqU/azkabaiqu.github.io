function changeRegion(region) {
    const regionInfo = regionData[region];
    if (regionInfo) {

        // Update for the value
        document.getElementById('total-value').querySelector('.desc-num').innerText = regionInfo.total.value;
        document.getElementById('destination-value').querySelector('.desc-num').innerText = regionInfo.destination.value;
        document.getElementById('accommodation-value').querySelector('.desc-num').innerText = regionInfo.accommodation.value;
        document.getElementById('entertainment-value').querySelector('.desc-num').innerText = regionInfo.entertainment.value;
        document.getElementById('culinary-place-value').querySelector('.desc-num').innerText = regionInfo.culinary.value;

        // Update 
        updateCardDisplay(regionInfo.total.card);
    }
}
// card model
function updateCardDisplay(cards) {
    const cardContainer = document.getElementById('card-container');
    cardContainer.innerHTML = '';

    cards.forEach(card => {
        const cardElement = document.createElement('a');
        cardElement.setAttribute('href', '#');
        cardElement.setAttribute('data-id', card.id);
        cardElement.addEventListener('click', () => {
            window.location.href = `detail-explore.html?id=${card.id}`;
        });
        cardElement.innerHTML = `

        <div class="card-loop sm:mx-2 mx-1 my-5 md:h-80 sm:h-72 h-60 md:w-72 sm:w-60 w-40 bg-white/30 hover:bg-white/50 overflow-hidden sm:rounded-2xl rounded-lg">
    <div class="md:h-52 sm:h-40 h-36 overflow-hidden">
        <img src="${card.imageUrl}" alt="gallery" class="w-full h-full object-cover">
    </div>
    <div class="px-5 font-trip-r">
        <h1 class="md:text-lg sm:text-base text-sm mt-3 truncate ">${card.title}</h1>
        <p class="md:text-sm text-xs  truncate mt-2"><i class="fa-solid fa-location-dot fa-sm"></i> ${card.location}</p>
        <p class="md:text-sm text-xs  "  style="color:#707070;"># ${card.highlight}</p>
    </div>
</div>           
        `;
        cardContainer.appendChild(cardElement);
    });
}

